int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0 || nb > 2147395600)
		return (0);
	while (i * i <= nb)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}
